"""End-to-end asset pipeline.

This script transforms the raw downloads under build/staging/ into a clean
set of .obj + .mtl + texture trios under assets/modelos/<name>/.

It handles three flavours of input:
1. Already-.obj models  -> copied, .mtl rewritten to point to the chosen
   diffuse texture, texture copied into the same folder.
2. .fbx and .blend models -> read with assimp_py (handles both formats),
   merged into a single mesh, written as .obj.
3. Models without any diffuse texture (Sci Fi Table, fish, chair if it
   cannot be extracted) -> a fallback texture is applied so every model
   ships with a usable map_Kd.

We also generate a procedural diffuse texture for the fish so that the
"todos os modelos com textura" rule is satisfied even without an
external download.
"""

from __future__ import annotations

import shutil
import sys
from pathlib import Path

import assimp_py as a
import numpy as np
from PIL import Image


PROJ_ROOT = Path(__file__).resolve().parent.parent
STAGING = PROJ_ROOT / "build" / "staging"
