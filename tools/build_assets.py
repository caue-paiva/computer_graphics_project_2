"""Pipeline de assets do projeto, ponta a ponta.

Este script transforma os downloads brutos que ficam em ``build/staging/``
(modelos baixados de free3d, CGTrader, BlendSwap, etc.) em um conjunto
limpo de trios ``.obj`` + ``.mtl`` + textura dentro de
``assets/modelos/<nome>/``, no formato exato que o runtime do projeto
espera carregar.

Ele lida com três sabores de entrada:

1. **Modelos que já vêm em ``.obj``** -> são copiados, o ``.mtl`` é
   reescrito para apontar para a textura difusa escolhida e a textura
   é copiada para a mesma pasta.
2. **Modelos em ``.fbx`` / ``.blend``** -> são lidos com ``assimp_py``
   (que entende os dois formatos), todas as malhas são unidas em um
   único mesh e o resultado é gravado como ``.obj``.
3. **Modelos sem nenhuma textura difusa** (mesa sci-fi, peixe-palhaço,
   cadeira nos casos em que ela não pode ser extraída) -> aplicamos
   uma textura procedural de fallback, garantindo que **todo** modelo
   saia daqui já com um ``map_Kd`` utilizável.

Também geramos uma textura difusa procedural para o peixe, de forma que
a regra "todos os modelos com textura" do edital seja atendida mesmo
sem nenhum download externo extra.
"""

from __future__ import annotations

import shutil
import sys
from pathlib import Path

import assimp_py as a
import numpy as np
from PIL import Image


PROJ_ROOT = Path(__file__).resolve().parent.parent
STAGING = PROJ_ROOT / "build" / "staging"
